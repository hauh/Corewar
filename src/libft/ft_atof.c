double	ft_atof(const char *s)
{
	double	n;
	double	d;
	int		sign;

	while ((*s >= 9 && *s <= 13) || *s == 32)
		++s;
	sign = (*s == '-' ? -1 : 1);
	if (*s == '-' || *s == '+')
		++s;
	n = 0;
	while (*s >= '0' && *s <= '9')
	{
		n = n * 10 + (*s - '0');
		++s;
	}
	if (*s == '.')
		++s;
	d = 10;
	while (*s >= '0' && *s <= '9')
	{
		n += (*s - '0') / d;
		d *= 10;
		++s;
	}
	return (n * sign);
}
